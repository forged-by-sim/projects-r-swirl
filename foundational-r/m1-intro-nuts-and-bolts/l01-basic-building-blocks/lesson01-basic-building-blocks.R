# Lesson 01: Basic Building Blocks

# Basic arithmetic
5 + 7

# Assign result to variable
x <- 5 + 7

# Print variable x
x

# Subtract 3 from x and assign to y
y <- x - 3

# Print variable y
y

# Create a numeric vector
z <- c(1.1, 9, 3.14)

# View documentation for c() function
?c

# View vector z
z

# Combine vectors
c(z, 555, z)

# Arithmetic with vector
z * 2 + 100

# Correct sqrt usage
my_sqrt <- sqrt(z - 1)

# View result of square root
my_sqrt

# Divide z by its square root
my_div <- z / my_sqrt

# View result of division
my_div

# Vector recycling example with different lengths
c(1, 2, 3, 4) + c(0, 10)
c(1, 2, 3, 4) + c(0, 10, 100)

# Fixing a typo or adjusting an earlier calculation
z * 2 + 1000

# Autocomplete variable example (typed 'my' + Tab)
my_div
